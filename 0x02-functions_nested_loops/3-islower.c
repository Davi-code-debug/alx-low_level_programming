#include "main.h"
/**
 * _islower - displays the value of lower and uppercases
 *int c: contains the letter
 *
 * display the return value of c
 * Return: 0 after success
 */
int main(void)
{
	int r;

	if (_islower(r))
	{
	_putchar(1 + '0');
	_putchar('\n');
	}
	else
	{
	_putchar(0 + '0');
	_putchar('\n');
	}
	return (0);
}
