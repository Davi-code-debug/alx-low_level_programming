#include "main.h"
/**
 * _islower - displays the value of lower and uppercases
 *c - contains the letter
 *
 * display the return value of c
 * Return: 0 after success
 */
int _islower(int c)
{
	if (_islower(c))
	{
	_putchar('1');
	_putchar('\n');
	}
	else
	{
	_putchar('0');
	_putchar('\n');
	}
	return (0);
}
